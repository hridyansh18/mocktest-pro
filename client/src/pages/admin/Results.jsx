import { useEffect, useMemo, useState } from 'react';
import {
  BarChart3,
  RefreshCw,
  FileSpreadsheet,
  FileText,
} from 'lucide-react';

import * as XLSX from 'xlsx';
import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';

import api from '../../services/api';

export default function Results() {
  const [tests, setTests] = useState([]);
  const [testId, setTestId] = useState('');
  const [rows, setRows] = useState([]);
  const [loading, setLoading] = useState(false);

  useEffect(() => {
    api.get('/admin/tests?limit=100').then((r) => {
      const testList = r.data.data.tests || [];

      setTests(testList);

      if (testList[0]) {
        setTestId(testList[0].id);
      }
    });
  }, []);

  const load = () => {
    if (!testId) return;

    setLoading(true);

    api
      .get(`/admin/tests/${testId}/results`)
      .then((r) => {
        setRows(r.data.data || []);
      })
      .finally(() => {
        setLoading(false);
      });
  };

  useEffect(() => {
    load();
  }, [testId]);

  const selectedTest = tests.find(
    (test) => test.id === testId
  );

  const average = useMemo(() => {
    if (!rows.length) return '0.0';

    return (
      rows.reduce(
        (total, row) =>
          total + Number(row.percentage || 0),
        0
      ) / rows.length
    ).toFixed(1);
  }, [rows]);

  const formatTime = (seconds) => {
    const value = Number(seconds || 0);

    return `${Math.floor(value / 60)}m ${
      value % 60
    }s`;
  };

  const exportExcel = () => {
    if (!rows.length) {
      alert('No result data available.');
      return;
    }

    const excelData = rows.map((row, index) => ({
      Rank: index + 1,
      Student: row.full_name,
      Enrollment: row.enrollment_number,
      Email: row.email,
      Status: row.passed ? 'Passed' : 'Not Passed',
      Score: row.score,
      Percentage: `${Number(
        row.percentage || 0
      ).toFixed(1)}%`,
      Correct: row.correct_count,
      Incorrect: row.incorrect_count,
      Unattempted: row.unattempted_count,
      'Time Taken': formatTime(
        row.time_taken_seconds
      ),
      Violations: row.violation_count,
    }));

    const worksheet =
      XLSX.utils.json_to_sheet(excelData);

    const workbook = XLSX.utils.book_new();

    XLSX.utils.book_append_sheet(
      workbook,
      worksheet,
      'Results'
    );

    worksheet['!cols'] = [
      { wch: 8 },
      { wch: 25 },
      { wch: 20 },
      { wch: 30 },
      { wch: 15 },
      { wch: 12 },
      { wch: 15 },
      { wch: 12 },
      { wch: 12 },
      { wch: 15 },
      { wch: 15 },
      { wch: 12 },
    ];

    const fileName =
      `${selectedTest?.title || 'MockTest'}-Results.xlsx`
        .replaceAll(' ', '-');

    XLSX.writeFile(workbook, fileName);
  };

  const downloadPDF = () => {
    if (!rows.length) {
      alert('No result data available.');
      return;
    }

    const doc = new jsPDF({
      orientation: 'landscape',
    });

    doc.setFontSize(20);

    doc.text(
      'MockTest Pro - Result Report',
      14,
      18
    );

    doc.setFontSize(12);

    doc.text(
      `Test: ${selectedTest?.title || 'Test'}`,
      14,
      28
    );

    doc.text(
      `Test Code: ${
        selectedTest?.test_code_id || '-'
      }`,
      14,
      35
    );

    doc.text(
      `Submitted Attempts: ${rows.length}`,
      14,
      42
    );

    doc.text(
      `Average Percentage: ${average}%`,
      80,
      42
    );

    doc.text(
      `Passed: ${
        rows.filter((row) => row.passed).length
      }`,
      155,
      42
    );

    autoTable(doc, {
      startY: 50,

      head: [
        [
          '#',
          'Student',
          'Enrollment',
          'Status',
          'Score',
          '%',
          'Correct',
          'Wrong',
          'Unattempted',
          'Time',
          'Violations',
        ],
      ],

      body: rows.map((row, index) => [
        index + 1,
        row.full_name,
        row.enrollment_number,
        row.passed ? 'Passed' : 'Not Passed',
        row.score,
        `${Number(
          row.percentage || 0
        ).toFixed(1)}%`,
        row.correct_count,
        row.incorrect_count,
        row.unattempted_count,
        formatTime(row.time_taken_seconds),
        row.violation_count,
      ]),

      styles: {
        fontSize: 8,
      },

      headStyles: {
        fillColor: [30, 41, 59],
      },
    });

    const finalY =
      doc.lastAutoTable?.finalY || 160;

    doc.setFontSize(9);

    doc.text(
      'Generated by MockTest Pro',
      14,
      finalY + 12
    );

    doc.text(
      'Designed & Developed by Hridyansh Chaudhary',
      14,
      finalY + 18
    );

    const fileName =
      `${selectedTest?.title || 'MockTest'}-Results.pdf`
        .replaceAll(' ', '-');

    doc.save(fileName);
  };

  return (
    <div>
      <h1 className="text-3xl font-bold">
        Results
      </h1>

      <p className="mt-2 text-sm text-slate-400">
        Submitted student attempts and scores.
      </p>

      <div className="glass mt-6 flex flex-wrap gap-3 rounded-2xl p-4">
        <select
          className="field max-w-xl"
          value={testId}
          onChange={(e) =>
            setTestId(e.target.value)
          }
        >
          <option value="">
            Select test
          </option>

          {tests.map((test) => (
            <option
              key={test.id}
              value={test.id}
            >
              {test.title} — {test.test_code_id}
            </option>
          ))}
        </select>

        <button
          onClick={load}
          className="btn-primary flex items-center gap-2"
        >
          <RefreshCw size={17} />

          Refresh
        </button>

        <button
          onClick={exportExcel}
          disabled={!rows.length}
          className="flex items-center gap-2 rounded-xl bg-emerald-500/10 px-4 py-2 text-sm font-semibold text-emerald-400 disabled:opacity-40"
        >
          <FileSpreadsheet size={18} />

          Export Excel
        </button>

        <button
          onClick={downloadPDF}
          disabled={!rows.length}
          className="flex items-center gap-2 rounded-xl bg-rose-500/10 px-4 py-2 text-sm font-semibold text-rose-400 disabled:opacity-40"
        >
          <FileText size={18} />

          Download PDF
        </button>
      </div>

      <div className="mt-5 grid gap-4 sm:grid-cols-3">
        <Card
          label="Submitted Attempts"
          value={rows.length}
        />

        <Card
          label="Average Percentage"
          value={`${average}%`}
        />

        <Card
          label="Passed"
          value={
            rows.filter((row) => row.passed)
              .length
          }
        />
      </div>

      <div className="glass mt-5 overflow-x-auto rounded-2xl">
        <table className="w-full min-w-[950px] text-left text-sm">
          <thead className="border-b border-white/10 text-xs uppercase text-slate-500">
            <tr>
              {[
                'Student',
                'Enrollment',
                'Email',
                'Status',
                'Score',
                'Percentage',
                'Correct',
                'Incorrect',
                'Unattempted',
                'Time',
                'Violations',
              ].map((heading) => (
                <th
                  key={heading}
                  className="px-4 py-4"
                >
                  {heading}
                </th>
              ))}
            </tr>
          </thead>

          <tbody>
            {rows.map((row) => (
              <tr
                key={row.attempt_id}
                className="border-b border-white/5"
              >
                <td className="px-4 py-4 font-medium">
                  {row.full_name}
                </td>

                <td className="px-4 py-4 text-slate-400">
                  {row.enrollment_number}
                </td>

                <td className="px-4 py-4 text-slate-400">
                  {row.email}
                </td>

                <td className="px-4 py-4">
                  <span
                    className={`rounded-full px-2 py-1 text-xs ${
                      row.passed
                        ? 'bg-emerald-500/10 text-emerald-400'
                        : 'bg-rose-500/10 text-rose-400'
                    }`}
                  >
                    {row.passed
                      ? 'Passed'
                      : 'Not Passed'}
                  </span>
                </td>

                <td className="px-4 py-4">
                  {row.score}
                </td>

                <td className="px-4 py-4">
                  {Number(
                    row.percentage || 0
                  ).toFixed(1)}
                  %
                </td>

                <td className="px-4 py-4 text-emerald-400">
                  {row.correct_count}
                </td>

                <td className="px-4 py-4 text-rose-400">
                  {row.incorrect_count}
                </td>

                <td className="px-4 py-4 text-amber-400">
                  {row.unattempted_count}
                </td>

                <td className="px-4 py-4">
                  {formatTime(
                    row.time_taken_seconds
                  )}
                </td>

                <td className="px-4 py-4">
                  {row.violation_count}
                </td>
              </tr>
            ))}
          </tbody>
        </table>

        {!loading && !rows.length && (
          <div className="py-16 text-center text-slate-500">
            <BarChart3 className="mx-auto mb-3" />

            <p>
              No submitted results for this test.
            </p>
          </div>
        )}
      </div>
    </div>
  );
}

function Card({ label, value }) {
  return (
    <div className="glass rounded-2xl p-5">
      <p className="text-sm text-slate-400">
        {label}
      </p>

      <p className="mt-2 text-3xl font-bold">
        {value}
      </p>
    </div>
  );
}